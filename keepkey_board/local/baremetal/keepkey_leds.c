//================================ INCLUDES ===================================


#include "keepkey_leds.h"
#include "pin.h"
#include <stddef.h>


//====================== CONSTANTS, TYPES, AND MACROS =========================


//=============================== VARIABLES ===================================

static const Pin GREEN_LED  = { GPIOC, GPIO14 };
static const Pin RED_LED    = { GPIOC, GPIO15 };

//====================== PRIVATE FUNCTION DECLARATIONS ========================



//=============================== FUNCTIONS ===================================


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
keepkey_leds_init(
        void
)
{
    pin_init_output( &GREEN_LED, PUSH_PULL_MODE, NO_PULL_MODE );
    pin_init_output( &RED_LED, PUSH_PULL_MODE, NO_PULL_MODE );

    clear_green();
    clear_red();
}


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
set_green(
        void
)
{
    CLEAR_PIN( GREEN_LED );
}


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
clear_green(
        void
)
{
    SET_PIN( GREEN_LED );
}


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
toggle_green(
        void
)
{
    TOGGLE_PIN( GREEN_LED );
}


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
set_red(
        void
)
{
    CLEAR_PIN( RED_LED );
}


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
clear_red(
        void
)
{
    SET_PIN( RED_LED );
}


//-----------------------------------------------------------------------------
/// 
///
//-----------------------------------------------------------------------------
void
toggle_red(
        void
)
{
    TOGGLE_PIN ( RED_LED );
}