//=============================== FUNCTIONS ===================================


const CharacterImage*
font_get_char(
        char c 
)
{
    const CharacterImage* img = NULL;

    int i = 0;
    while( ( i < font.length ) && ( img == NULL ) )
    {
        if( font.characters[ i ].code == c )
        {
            img = font.characters[ i ].image;
        }

        i++;
    }

    return img;
}


int
font_height(
        void
)
{
    return font.size;
}

int font_width(void)
{
    /**
     * Return worst case width using the space char as the reference.
     */
    return  font_get_char(' ')->width;
}
